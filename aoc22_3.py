import string

char_values = dict(zip(string.ascii_lowercase + string.ascii_uppercase, range(1,53)))

def calc_bags(inputs: list):
    
    sum = 0
    for input in inputs:
        input_len_half = len(input) // 2
        first, second = set(input[0:input_len_half]), set(input[input_len_half:])

        intersect = first.intersection(second)
        for k in intersect:
            sum += char_values[k]
        
    return sum


def calc_groups(inputs: list):

    sum = 0

    for i in range(0, len(inputs), 3):
        i1, i2, i3 = inputs[i:i+3]
        common = set(i1).intersection(set(i2).intersection(i3))
        sum += char_values[common.pop()]

    return sum


